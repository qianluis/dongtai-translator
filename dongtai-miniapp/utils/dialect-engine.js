function correctASR(text) {
  let result = text;
  for (const [wrong, right] of ASR_FIX) {
    if (result.includes(wrong) && wrong !== right) {
      result = result.replace(new RegExp(wrong, 'g'), right);
    }
  }
  // "不"+动词 → "勿"+动词
  result = result.replace(/不(?=[走做说吃看想会用能好去来])/g, '勿');
  return result;
}

function detectDirection(text) {
  let count = 0;
  for (const m of DIALECT_MARKERS) {
    if (text.includes(m)) count++;
  }
  return count >= 1 ? 'd2m' : 'm2d';
}

function m2d(text) {
  let result = text;
  for (const [src, dst] of M2D) {
    result = result.replace(new RegExp(src, 'g'), dst);
  }
  // 句尾语气词
  for (const [suffix, dSuffix] of Object.entries(SUFFIX_M2D)) {
    if (result.endsWith(suffix)) {
      result = result.slice(0, -suffix.length) + dSuffix;
      break;
    }
  }
  return result;
}

function d2m(text) {
  let result = text;
  for (const [src, dst] of D2M_WORDS) {
    result = result.replace(new RegExp(src, 'g'), dst);
  }
  // 句尾语气词
  for (const [dSuffix, suffix] of Object.entries(SUFFIX_D2M)) {
    if (result.endsWith(dSuffix)) {
      result = result.slice(0, -dSuffix.length) + suffix;
      break;
    }
  }
  return result;
}

function translate(text, direction = 'auto', asrMode = false) {
  if (!text || !text.trim()) return { result: '', direction: 'unknown', confidence: 0 };
  text = text.trim();
  const original = text;

  // 语音纠错
  if (asrMode || direction === 'auto') {
    const corrected = correctASR(text);
    if (corrected !== text) text = corrected;
  }

  // 检测方向
  if (direction === 'auto') direction = detectDirection(text);

  const result = direction === 'm2d' ? m2d(text) : d2m(text);
  const changed = [...text].filter((c, i) => c !== result[i]).length;
  const confidence = Math.min(100, Math.round(changed / Math.max(1, text.length) * 300 + 50));

  return {
    result,
    direction,
    directionLabel: direction === 'd2m' ? '方言→普通话' : '普通话→方言',
    confidence,
    asrCorrected: original !== text
  };
}

